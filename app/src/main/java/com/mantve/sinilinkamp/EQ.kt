package com.mantve.sinilinkamp

enum class EQ (val payload: ByteArray){
    Normal(byteArrayOf(0x7e,0x05,0x09,0x00, 0x8c.toByte())),
    Rock(byteArrayOf(0x7e,0x05,0x0a,0x00, 0x8d.toByte())),
    Pop(byteArrayOf(0x7e,0x05,0x0b,0x00, 0x8e.toByte())),
    Classic(byteArrayOf(0x7e,0x05,0x0c,0x00, 0x8f.toByte())),
    Jazz(byteArrayOf(0x7e,0x05,0x0d,0x0, 0x90.toByte())),
    Country(byteArrayOf(0x7e,0x05,0x0e,0x00, 0x91.toByte())),
}